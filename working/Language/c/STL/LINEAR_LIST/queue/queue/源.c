                                                               //循环队列（头出之后就让它空着，首尾相接以充分利用空间）
#define MAXSIZE 10
#define ERROR 0
#define OK 1
typedef int status;
typedef struct {
    int  arr[MAXSIZE];
    int head;//指向头元素
    int tail;//指向尾元素的下一个位置
} QUEUE;


void init_queue(QUEUE* queue) {
    queue->head = 0;//head和tail相等就代表队列为空。那么队列满了也是相等就不好判断
    queue->tail = 0;//所以规定能够使用的空间最多为数组大小减一，这样当head和tail相等时就只可能是空队列了。
}
int get_length(QUEUE* queue) {
    return (queue->tail - queue->head + MAXSIZE) % MAXSIZE;
}
status get_in(QUEUE* queue, int num) {
    if ((queue->tail + 1) % MAXSIZE == queue->head) {
        return ERROR;
    }
    queue->tail = num;
    queue->tail = (queue->tail + 1) % MAXSIZE;
    return OK;
}
status get_out(QUEUE* queue, int* num) {
    if (queue->head == queue->tail) {
        return ERROR;
    }
    *num = queue->head;
    queue->head = (queue->head + 1) % MAXSIZE;
    return OK;
}


int main()
{
    QUEUE queue;
    init_queue(&queue);

    return 0;
}
























